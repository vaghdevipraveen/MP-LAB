;PROGRAM TO EXCHANGE THE CONTENTS OF 2 ARRAYS ELEMENT BY ELEMENT

.MODEL SMALL
.DATA
A DB 10H, 20H, 30H, 40H, 50H
B DB 11H, 22H, 33H, 44H, 55H

.CODE

MOV AX, @DATA
MOV DS, AX

LEA SI, A  ; SI WILL HAVE STARTING ADDRESS OF A
LEA DI, B  ; DI WILL HAVE STARTING ADDRESS OF B

MOV CL, 05H

;LOOP DEFINITION

BACK: MOV AL, [SI]   ; [SI] RETRIVES THE ELEMENT PRESENT THE ADDRESS IN SI
      XCHG AL, [DI]  ; EXCHANGES THE ELEMENTS IN AL AND [DI]
      MOV [SI], AL

INC SI   ; SI=SI+1
INC DI
LOOP BACK ; CX=CX-1 ; IF CX!=0, THEN JUMP BACK TO TARGET

; TO VERIFY THE OUTPUT

LEA SI, A
LEA DI, B

MOV BL, [SI]

MOV BH, [DI]

MOV AH, 4CH
INT 21H
END